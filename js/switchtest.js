var choice = prompt("What is your favorite flavor of ice cream?");

switch(choice) {
case 'vanilla':
console.log("I love vanilla, too!");
break;
case 'chocolate':
console.log("That's one of my favorites, too!");
break;
case 'rocky road':
console.log("A lot of people like that one but not me.");
break;
case 'strawberry':
console.log("That's a good one!");
break;
case 'cookies and cream':
console.log("Oh that is a yummy flavor!");
break;
default:
console.log("I don't think I've had that one.");
break;
}
